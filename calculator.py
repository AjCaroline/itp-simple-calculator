def add(x, y):
    return x + y


def subtract(x, y):
    return x - y


def divide(x, y):
    return x / y


def divide_by_zero(x, y):
    results = divide(x, y)
    assert type(results) == str
    return results
    

def multiply(x, y):
    return x * y


def square(x):
    return square(x, y) 


def power(x, y):
    return power(x, y)


def sqrt(x):
    return sqrt(x, y)
    
